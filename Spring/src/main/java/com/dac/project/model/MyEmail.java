package com.dac.project.model;

public class MyEmail {
	
	
		private String email;
		
		
		

		public MyEmail() {
			super();
		}

		public String getEmail() {
			return email;
		}

		public void setEmail(String email) {
			this.email = email;
		}

		@Override
		public String toString() {
			return "MyEmail [email=" + email + "]";
		}




}
